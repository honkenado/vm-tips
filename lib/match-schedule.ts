function getGroupLetter(groupName: string) {
  return groupName.replace("Grupp ", "").trim();
}

function getDateSortValue(date: string, time: string) {
  const [dayText, monthText] = date.split(" ");
  const day = Number(dayText);
  const monthMap: Record<string, number> = {
    juni: 5,
    juli: 6,
  };

  const month = monthMap[monthText] ?? 0;
  const [hour, minute] = time.split(":").map(Number);

  return new Date(2026, month, day, hour, minute).getTime();
}

export function getGroupStageSchedule(): ScheduleMatch[] {
  const groupSchedule = initialGroups.flatMap((group) =>
    group.matches.map((match) => ({
      id: `group-${match.matchNumber}`,
      matchNumber: match.matchNumber,
      date: match.date,
      time: timeOverrideByMatchNumber[match.matchNumber] ?? match.time,
      homeTeam: match.homeTeam,
      awayTeam: match.awayTeam,
      stage: "group" as const,
      groupLetter: getGroupLetter(group.name),
      groupName: group.name,
      tvChannel: tvChannelByMatchNumber[match.matchNumber] ?? DEFAULT_TV_CHANNEL,
      streamingChannel:
        streamingChannelByMatchNumber[match.matchNumber] ??
        DEFAULT_STREAMING_CHANNEL,
    }))
  );

  return [...groupSchedule, ...knockoutSchedule].sort(
    (a, b) =>
      getDateSortValue(a.date, a.time) - getDateSortValue(b.date, b.time)
  );
}

export function getScheduleMatchByNumber(matchNumber: number) {
  return getGroupStageSchedule().find(
    (match) => match.matchNumber === matchNumber
  );
}