const streamingChannelByMatchNumber: Record<number, string> =
  Object.fromEntries(
    Object.entries(tvChannelByMatchNumber).map(([key, channel]) => [
      Number(key),
      channel === "SVT" ? "SVT Play" : "TV4 Play",
    ])
  );

